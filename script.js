function parseMeasurements(input) {
    return input.split('\n').map(line => {
        const value = parseFloat(line);
        const unit = line.match(/[a-zA-Z]+/)[0];
        return { value, unit };
    });
}

// Convert to cm
function convertToCm(value, unit) {
    const conversions = {
        'ft': 30.48,
        'in': 2.54,
        'mil': 160934,
        'yd': 91.44,
        'wm': 60
    };
    return value * conversions[unit];
}


